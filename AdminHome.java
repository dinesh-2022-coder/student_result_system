    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        String url,user,pass;
        
        url= "jdbc:mysql://localhost:3306/StudentResult";
        user = "root";
        pass = "1234";
        
        
        String course = (String) c1.getSelectedItem();
        String branch = (String) c2.getSelectedItem();
        String rollnum = tx1.getText();
        String name = tx2.getText();
        String gender = (String) c3.getSelectedItem();
        String fathersname = ftx.getText();
        
//        String updateQuery = "select * from student where rollno='"+rollnum+"'";
        String dataupdate =  "INSERT INTO student values ('"+ rollnum +"','"+branch+"','"+course+"','"+name+"','"+gender+"','"+fathersname+"')";
        
        try{
            Connection con = DriverManager.getConnection(url,user,pass);
            Statement st = con.createStatement();
            st.executeUpdate(dataupdate);
            JOptionPane.showMessageDialog(this, "Information updated");
            setVisible(false);
            new AdminHome().setVisible(true);
            }     
 
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "This rollno is already exist");
            setVisible(false);
            new AdminHome().setVisible(true);
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

