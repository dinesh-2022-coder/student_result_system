    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      
        String rollno, physics, chemistry, maths, electronic, electrical;
        
        rollno = tx1.getText();
        physics = tx2.getText();
        chemistry = tx3.getText();
        maths = tx4.getText();
        electronic = tx5.getText();
        electrical =tx6.getText();
        
        String selectquery = "select * from student where rollno='"+rollno+"'";
        String dataupdate =  "INSERT INTO result values ('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+electronic+"','"+electrical+"')";

        try{
            
        Connection con = AdminHome.getConnection();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(selectquery);
        
        if(rs.next()){
            st.executeUpdate(dataupdate);
            JOptionPane.showMessageDialog(this, "Information updated");
            setVisible(false);
            new InsertResult().setVisible(true);
        }
        
        else{
                        JOptionPane.showMessageDialog(this, "Connection error");
                        setVisible(false);
            new InsertResult().setVisible(true);
        }

        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "roll no already exist");
                setVisible(false);
            new InsertResult().setVisible(true);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

